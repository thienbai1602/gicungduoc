ll a, b, K;

void solve()
{
    cin >> a >> b >> K;
    ll u = ceil(sqrt(a)), v = ceil(cbrt(a)), ans = 0;
    for(ll x=u; x*x<=b; x++)
    {
        for(ll y=v; y*y*y<=b; y++)
        {
            if (abs(x * x - y * y * y) <= K) ++ans;
        }
    }
    cout << ans;
}

