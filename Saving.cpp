const int mxN = 1e6 + 66;

ll N;

ll bs(ll l, ll r)
{
    ll ans = 0;
    while(l <= r)
    {
        ll m = (l + r) >> 1;
        ll X = (m + 1) * m / 2;
        if (X >= N)
        {
            ans = m;
            r = m - 1;
        } else
        {
            l = m + 1;
        }
    }
    return ans;
}

void solve()
{
    cin >> N;
    cout << bs(1, 1e10);
}

